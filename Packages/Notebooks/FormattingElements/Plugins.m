(*GetPluginData*)



GetPluginData[]:=
  Module[
    {baseData},
    baseData=
      Map[
        LoadPlugin,
        FileNames[__~~(".wl"|".m"), 
          FileNames["Plugins", $IDESettingsPath]
          ]
        ];
    baseData =
      GroupBy[baseData, #Type&];
    If[!KeyExistsQ[baseData, "Menu"], baseData["Menu"]={}];
    If[!KeyExistsQ[baseData, "Commands"], baseData["Commands"]={}];
    baseData
    ]


