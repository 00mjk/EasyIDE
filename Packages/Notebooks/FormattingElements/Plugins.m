(*createPluginCommandsMenu*)



createPluginCommandsMenu[s_, data_]:=
  (
    If[!AssociationQ[s], s=<||>];
    If[KeyExistsQ[s, "RootCell"],
      DestroyDropDownMenu[Dynamic[s]],
      AttachDropDownMenu[
        Dynamic[s], 
        EvaluationBox[], 
        Lookup[data["Commands"], "Commands", {}]
        ]
      ]
    );
createPluginCommandsMenu~SetAttributes~HoldFirst


