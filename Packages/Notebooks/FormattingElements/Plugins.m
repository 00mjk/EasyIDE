LoadPlugin//Clear
LoadPlugin[file_String, default_:Automatic]:=
  Module[
    {
      pluginData
      },
    PackageExecute[
      Internal`WithLocalSettings[
        BeginPackage["`Plugins`"];
        $ContextPath = 
          Join[
            {"System`"},
            $PackageContexts, 
            Append[$ContextPath, $Context]
            ];,
        pluginData = Get[file],
        EndPackage[]
        ]
      ];
    pluginData=
      Replace[pluginData, 
        {
          a_Association?(KeyExistsQ[#, "Name"]&&(
            KeyExistsQ[#, "Commands"]||
            KeyExistsQ[#, "Menu"]||
            KeyExistsQ[#, "Toolbar"]
            )&):>
            a,
          pd_?(StringQ[default]&):>
            <|
              "Name"->FileBaseName[file],
              default->Normal@pd
              |>,
          pd:{(_Rule|_RuleDelayed)..}|_Association:>
            <|
              "Name"->FileBaseName[file],
              "Commands"->Normal@pd
              |>,
          pd:{(Delimiter|_RuleDelayed)..}:>
            <|
              "Name"->FileBaseName[file],
              "Menu"->pluginData
              |>,
          _List:>
            <|
              "Name"->FileBaseName[file],
              "Toolbar"->pluginData
              |>,
          m_:>
            <|
              "Name"->FileBaseName[file],
              "Menu"->m
              |>
          }
        ];
    If[!KeyExistsQ[pluginData, "Type"],
      pluginData["Type"]=
        Which[
          KeyExistsQ[pluginData, "Commands"],
            "Commands",
          KeyExistsQ[pluginData, "Toolbar"],
            "Toolbar",
          True,
            "Menu"
          ]
      ];
    If[pluginData["Type"]==="Commands",
      pluginData["Commands"]=
        pluginData["Name"]->pluginData["Commands"]
      ];
    normalizePlugin@pluginData
    ]


