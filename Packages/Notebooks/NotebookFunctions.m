ideNotebookSave//Clear
Module[{recurseProtect},
  ideNotebookSave[nb_NotebookObject, 
    file:_String|Automatic:Automatic,
    preemptive:True|False:False
    ]:=
    Block[{recurseProtect = !preemptive&&TrueQ[recurseProtect]},
      If[!recurseProtect,
        recurseProtect=True;
        Module[
          {
            f = file,
            dir,
            nbExpr,
            nbObj
            },
          If[f === Automatic,
            f = ideActiveFile[nb]
            ];
          If[f=!=None,
            f = ideAbsPath[nb, f];
            Switch[FileExtension[f],
              "nb",
                nbExpr = ideNotebookExpr[nb];
                If[preemptive,
                  premptiveQueuedEval[nb, Export[f, nbExpr]]
                  ] (* should I just Put this? *),
              "m"|"wl",
                If[preemptive,
                  nbExpr =ideNotebookExpr[nb];
                  premptiveQueuedEval[nb,
                    Internal`WithLocalSettings[
                      nbObj = CreateDocument[nbExpr, Visible->False],
                      FrontEndExecute@
                        FrontEndToken[
                          nbObj,
                          "Save",
                          {f, "Package"}
                          ],
                      NotebookClose[nbObj]
                      ]
                    ],
                  nbObj = nb;
                  FrontEndExecute@
                    FrontEndToken[
                      nbObj,
                      "Save",
                      {f, "Package"}
                      ]
                  ],
              _,
                If[preemptive,
                  nbExpr =ideNotebookExpr[nb];
                  premptiveQueuedEval[nb,
                    Internal`WithLocalSettings[
                      nbObj = CreateDocument[nbExpr, Visible->False],
                      FrontEndExecute@
                        FrontEndToken[
                          nbObj,
                          "Save",
                          {f, "Text"}
                          ],
                      NotebookClose[nbObj]
                      ]
                    ],
                  nbObj = nb;
                  FrontEndExecute@
                    FrontEndToken[
                      nbObj,
